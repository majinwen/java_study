public class Score {
    private int chinese;
    private int english;
    private int math;
    public void setChinese(int chinese){
        this.chinese=chinese;
    }
    public void setEnglish(int english){
        this.english=english;
    }
    public void setMath(int math){
        this.math=math;
    }

    public int getChinese() {
        return chinese;
    }

    public int getEnglish() {
        return english;
    }

    public int getMath() {
        return math;
    }
}
